    <ul>
        <li>A <a href="#written-application">written application</a> submitted by <strong>Tuesday, January 31st</strong></li>
        <li>Attendance at one of the three selected <a href="#gtky-events">“Get to Know You” (GTKY)</a> Events</li>
        <li>Attendance at the <a href="#first-social">first social</a> on <strong>Monday, February 6th</strong></li>
        <li><a href="#interviews">Interviews</a>, <strong>February 7th - February 9th</strong></li>
        <li>Attendance at the <a href="#second-social">second social</a> on <strong>Saturday, February 11th</strong> (by invitation only)</li>
        <li>Attendance at the all-club Spring Retreat (Date TBD if selected to be a 2023 probate)</li>
    </ul>

    <p>For details about each of the above requirements, check out the sections below or click on the requirement for more information!</p>

    <p>Any absences or potential conflicts with the required events should be brought to the attention of <a href="mailto:membershipchair@reckclub.org">Charlie Hammer and Keshav Ramanathan</a>, Ramblin’ Reck Club Membership Co-Chairs, no later than Tuesday, January 31st.</p>

<h4 class="mb-2">Probate Requirements</h4>

<p>Before applying be sure to read the following requirements:</p>

<ul>
    <li>Applicants may <strong>not</strong> graduate prior to December 2023.</li>
    <li>Applicants must be able to attend a weekly probate meeting on Monday evenings from 6-7 PM and then the full club meeting at 7 PM. Exceptions to this can be made, but very rarely.</li>
    <li>Spring 2023 out-of-town co-ops and study abroad students are not eligible to apply this RECKruitment season. Fall 2023 out-of-town co-ops and study abroad students will be dealt with on a case by case basis. Email <a href="mailto:membershipchair@reckclub.org">Charlie Hammer and Keshav Ramanathan</a>, Ramblin’ Reck Club Membership Co-Chairs, if you have questions or concerns.</li>
</ul>

<h4 class="mb-2">Tips for RECKruitment</h4>

<ul>
    <li>Be yourself! We want to get to know you and why you love Georgia Tech!</li>
    <li>Have fun! The socials and "Get To Know You" events will have fun activities and should not feel like an interview; that's what the interview is for.</li>
    <li>Meet as many members as possible across the socials and "Get To Know You" events. Don't be afraid to ask a member you already know to introduce you to someone new.</li>
    <li>Put time and effort into your application. Follow the suggested word counts for each question to make sure you answer it fully.</li>
    <li>For any Reck Club Reckruitment event, wear something that you feel comfortable and confident in! There are no specifications on dress code for any event, including the interviews. If you would like assistance with clothing, <a href="https://career.gatech.edu/campus-closet-open" target="blank">Georgia Tech's Campus Closet</a> provides this service free of charge to students!</li>
</ul>

