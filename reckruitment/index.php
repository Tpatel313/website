<h4 class="mb-2" id="first-social">First Social (Mandatory)</h4>
    <p>Attending this social is your chance as an applicant to meet Reck Clubbers, get a feel for what Ramblin’ Reck Club is all about, ask any questions you may have about the Club or getting involved, and most importantly, sign up for an interview time. No need to stress about this event; be yourself and talk to anyone you do not already know! This event will be hosted in-person at the <a href="https://studentcenter.gatech.edu/exhibition-hall-map" target="blank"> Midtown Room<a/> in the Exhibition Hall.</p>
    <ul>
        <li><strong>First Social: Monday, February 6th</strong></li>
    </ul>
    <p>There are two time slots for the first social: <strong>8:00pm-9:00pm and 9:30pm-10:30pm</strong>. Please note down your availability on the application. If you are not available during either of these times, please email <a href="mailto:membershipchair@reckclub.org">Charlie Hammer and/or Keshav Ramanathan</a>.</p>

<h4 class="mb-2" id="interviews">Interviews (Mandatory)</h4>

    <p>The interview portion of RECKruitment is designed to allow for Reck Clubbers to get to know applicants on a more personal basis. We just want to give each applicant the time to express their ideas, commitment, and personality. The interview portion consists of an individual interview and a group interview. Interviewees will sign up for an interview time slot during check-in at the first social. All interviews will be at the <strong>Exhibition Hall</strong>. Please arrive at the <a href="https://studentcenter.gatech.edu/exhibition-hall-map" target="blank"> Old 4th Ward Room<a/> for your interviews.</p>
    <ul>
        <li><strong>Tuesday, February 7th from 6:00pm-10:00pm</strong></li>
        <li><strong>Wednesday, February 8th from 5:00pm-10:00pm</strong></li>
        <li><strong>Thursday, February 9th from 8:00pm-11:00pm</strong></li>
    </ul>

<h4 class="mb-2" id="second-social">Second Social (Mandatory)</h4>
    <p>After all interviews have been conducted, the Membership Chairs will notify each applicant whether they will advance to the second round of RECKruitment. During this second round, applicants will attend a mandatory second social held from <strong>1:30pm-3:30pm on Saturday, February 11th</strong>. Further details for this event will be sent out to those invited applicants only.</p>

